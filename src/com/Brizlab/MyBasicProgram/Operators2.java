package com.Brizlab.MyBasicProgram;

public class Operators2 {
    public static void main(String[] args) {
        // Arithmetic operators
        int a = 10;
        int b = 5;

        System.out.println("a+b = " + (a+b));
        System.out.println("a-b = " + (a-b));
        System.out.println("a*b = " + (a*b));
        System.out.println("a/b = " + (a/b));
        System.out.println("a%b = " + (a%b));
    }
}
