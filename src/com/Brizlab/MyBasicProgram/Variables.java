package com.Brizlab.MyBasicProgram;

public class Variables {

    public static class Adding {
        public static void main(String[] args) {
            int a = 10;
            int b = 25;
            int sum = a+b;
            System.out.println(sum);
        }
    }

    public static class Substraction {
        public static void main(String[] args) {
            int a =10;
            int b = 23;
            int sum = a-b;
            System.out.println(sum);
        }
    }
}
